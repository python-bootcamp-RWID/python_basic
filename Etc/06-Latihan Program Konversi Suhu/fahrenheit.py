#  program konversi fahrenheit ke satuan lain

#  fahrenheit
print("\n==== PROGRAM KONVERSI FAHRENHEIT TO SATUAN LAIN ====\n")
fahrenheit = float(input("masukan nilai fahrenheit : "))
print("fahrenheit adalah : ", fahrenheit, " Fahrenheit")

#  konversi ke kelvin
kelvin = (fahrenheit + 459.67) * 5/9
print("suhu dalam satuan kelvin adalah ", kelvin, "Kelvin")

