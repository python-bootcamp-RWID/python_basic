# belajar fungsi input user
# data yang di input akan berupa string

name = input("What is your name : ")
umur = input("How old are you  : ")
print("My name is : ", name)
print(f"I am {umur} years old")

# konversi / parcing to integer
nilai_a = int(input("masukan nilai a : "))
nilai_b = int(input("masukan nilai b : "))
jumlah = nilai_a + nilai_b
print(nilai_a, " + ", nilai_b, " = ", jumlah)

# konversi / parcing to float
nilai_a = float(input("masukan nilai a : "))
nilai_b = float(input("masukan nilai b : "))
bagi = nilai_a / nilai_b
print(nilai_a, " / ", nilai_b, " = ", bagi)



